#ifndef _H_pthread
#define _H_pthread

# include "pthread_vita.h"

#define PTHREAD_PROCESS_ATTACH()
#define PTHREAD_THREAD_ATTACH()
#define PTHREAD_PROCESS_DETACH()
#define PTHREAD_THREAD_DETACH()

// Assume that pthread threads will have default CPU affinity, i.e. any CPU and so should use the common queue priorities by default.
#define PTHREAD_DEFAULT_PRIORITY_USE_COMMON_QUEUE	1

#if PTHREAD_DEFAULT_PRIORITY_USE_COMMON_QUEUE
#define PTHREAD_DEFAULT_PRIORITY			(SCE_KERNEL_COMMON_QUEUE_HIGHEST_PRIORITY + (SCE_KERNEL_COMMON_QUEUE_LOWEST_PRIORITY - SCE_KERNEL_COMMON_QUEUE_HIGHEST_PRIORITY) / 2)
#define PTHREAD_DEFAULT_PRIORITY_CEILING	(PTHREAD_DEFAULT_PRIORITY - 1)
#else
#define PTHREAD_DEFAULT_PRIORITY			(SCE_KERNEL_INDIVIDUAL_QUEUE_HIGHEST_PRIORITY + (SCE_KERNEL_INDIVIDUAL_QUEUE_LOWEST_PRIORITY - SCE_KERNEL_INDIVIDUAL_QUEUE_HIGHEST_PRIORITY) / 2)
#define PTHREAD_DEFAULT_PRIORITY_CEILING	(PTHREAD_DEFAULT_PRIORITY - 1)
#endif

#define PTHREAD_DEFAULT_AFFINITY			(SCE_KERNEL_CPU_MASK_USER_ALL)

#endif /* _H_pthread */
